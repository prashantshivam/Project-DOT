package com.dot.Project;

public class ProjectLink {
int pid;
String plink;
/**
 * @param pid
 * @param plink
 */
public ProjectLink(int pid, String plink) {
	super();
	this.pid = pid;
	this.plink = plink;
}
public int getPid() {
	return pid;
}
public void setPid(int pid) {
	this.pid = pid;
}
public String getPlink() {
	return plink;
}
public void setPlink(String plink) {
	this.plink = plink;
}

	
	
	
	
	
	
	
}
