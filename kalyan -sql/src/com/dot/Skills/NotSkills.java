package com.dot.Skills;

public class NotSkills {
int sid;
String skill;
/**
 * @param sid
 * @param skill
 */
public NotSkills(int sid, String skill) {
	super();
	this.sid = sid;
	this.skill = skill;
}


public int getSid() {
	return sid;
}
public void setSid(int sid) {
	this.sid = sid;
}
public String getSkill() {
	return skill;
}
public void setSkill(String skill) {
	this.skill = skill;
}

}
